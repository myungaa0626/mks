package Algorithm;

public class SwitchTest {

	public static void main(String[] args) {
		int num = 2;
		switch (num) {
		case 1 :
			System.out.println("Good Morning, Java");
			break;
			
		case 2 : 
			System.out.println("hello");
			break;
		
		case 3 :
			System.out.println("bye");
			break;
			
		default :
			System.out.println("goodbye");
			break;
		
		
		}
		//System.out.println("done.");
	
		
		

	}

}
